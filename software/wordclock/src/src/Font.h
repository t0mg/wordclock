// A monospace font table structre.
struct FontTable
{
  int characterWidth;
  int characterHeight;
  char firstChar;
  std::vector<std::vector<byte>> data; // ASCII data.
  static std::vector<byte> getCharData(FontTable fontTable, char c)
  {
    int index = c - fontTable.firstChar;
    if (index < 0 || index >= fontTable.data.size())
    {
      return std::vector<byte>();
    }
    return fontTable.data.at(index);
  };
};

